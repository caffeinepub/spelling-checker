const DICT_ARRAY = Array.from(DICTIONARY);

/**
 * Levenshtein distance between two strings.
 */
export function levenshtein(a: string, b: string): number {
  const m = a.length;
  const n = b.length;
  if (m === 0) return n;
  if (n === 0) return m;
  let prev = Array.from({ length: n + 1 }, (_, i) => i);
  let curr = new Array(n + 1).fill(0) as number[];
  for (let i = 1; i <= m; i++) {
    curr[0] = i;
    for (let j = 1; j <= n; j++) {
      const cost = a[i - 1] === b[j - 1] ? 0 : 1;
      curr[j] = Math.min(curr[j - 1] + 1, prev[j] + 1, prev[j - 1] + cost);
    }
    [prev, curr] = [curr, prev];
  }
  return prev[n];
}

/**
 * Get top suggestions for a misspelled word.
 */
export function getSuggestions(word: string, count = 5): string[] {
  const lower = word.toLowerCase();
  const scored = DICT_ARRAY.map((dictWord) => ({
    word: dictWord,
    dist: levenshtein(lower, dictWord),
  }));
  scored.sort((a, b) => a.dist - b.dist);
  return scored.slice(0, count).map((s) => s.word);
}

export interface SpellError {
  word: string;
  index: number;
  suggestions: string[];
}

/**
 * Parse text into tokens preserving positions.
 */
function tokenize(text: string): Array<{ word: string; start: number }> {
  const tokens: Array<{ word: string; start: number }> = [];
  const regex = /[a-zA-Z']+/g;
  let match: RegExpExecArray | null = regex.exec(text);
  while (match !== null) {
    tokens.push({ word: match[0], start: match.index });
    match = regex.exec(text);
  }
  return tokens;
}

/**
 * Run spell check on text, returning errors.
 */
export function checkSpelling(text: string): SpellError[] {
  const tokens = tokenize(text);
  const errors: SpellError[] = [];
  const seen = new Set<string>();
  for (const { word, start } of tokens) {
    const clean = word.replace(/^'+|'+$/g, "");
    if (clean.length < 2) continue;
    const lower = clean.toLowerCase();
    if (DICTIONARY.has(lower)) continue;
    if (seen.has(lower)) continue;
    seen.add(lower);
    errors.push({
      word: clean,
      index: start,
      suggestions: getSuggestions(lower),
    });
  }
  return errors;
}
